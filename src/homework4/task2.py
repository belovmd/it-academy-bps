# Города Дан список стран и городов каждой страны. Затем даны названия городов. Для каждого города укажите,
# в какой стране он находится. Входные данные Программа получает на вход количество стран N. Далее идет N строк,
# каждая строка начинается с названия страны, затем идут названия городов этой страны. В следующей строке записано
# число M, далее идут M запросов — названия каких-то M городов, перечисленных выше. Выходные данные Для каждого из
# запроса выведите название страны, в котором находится данный город
dct = {}
for i in range(int(input())):
    country, *city = input().split()
    dct[country] = city
list2 = []
for j in range(int(input())):
    city = input()
    list2.append(city)
    for key, value in dct.items():
        if list2[j] in value:
            print(key)
