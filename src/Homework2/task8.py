# 7. Написать калькулятор по сложным процентам по кредиту
amount = int(input('Сколько хотите взять денег: '))
prow = int(input('Под какой процент : '))
years = float(input('На сколько лет: '))
prow = prow / 100
month_pay = (amount * prow * (1 + prow)**years) / (12 * ((1 + prow)**years - 1))
summa = month_pay * years * 12
print('Ваш месячный платеж составит: ', round(month_pay, 2))
print('За весь период вы заплатите: ', round(summa, 2))


