# 3. Вывести степени натуральных чисел, не превосходящие данного числа n.
# Пользователь задает показатель степени и число n.
p = int(input('Показатель степени: '))
n = int(input('Максимальное число: '))
i = 1
while i ** p <= n:
    i += 1
print('Последнее число,возведенное в степень', i - 1)

# 4. Наибольший общий делитель (НОД) – это число, которое делит без остатка два числа
# и делится само без остатка на любой другой делитель данных двух чисел.
# Найти НОД для вводимых чисел.
a = int(input('Введите число А: '))
b = int(input('Введите число B: '))
while a != b:
    if a > b:
        a = a - b
    else:
        b = b - a
print(a)

# 5. Определить, сколько в числе четных цифр, а сколько нечетных. Число вводится с клавиатуры.
